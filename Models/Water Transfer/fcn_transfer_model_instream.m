function subctch_em_out = fcn_transfer_model_instream(subctch_em_in)
    
    %% (1) Predict from instream models
    %  =================================
    % Simple formulation without calling to R (for speed)
    % Coefficients are hard coded in!
    % Store in subctch_em_out structure
    % Each model has log(response + 1) link function
    
    % (a) flow
    % --------
    subctch_em_out.flow = 0.03836072 + ...
                          0.92106781 * subctch_em_in.flow;
    
    % (b) orgn
    % --------
    subctch_em_out.orgn = 12.7754906 + ...
                          0.8501977 * subctch_em_in.orgn + ...
                          11.8430254 * subctch_em_in.flow;
    
    % (c) orgp
    % --------
    subctch_em_out.orgp = 4.0271632 + ...
                          0.8170857 * subctch_em_in.orgp + ...
                          3.7839536 * subctch_em_in.flow;
    
    % (d) no3
    % -------
    subctch_em_out.no3 = 0.9330308 + ...
                         0.8333475 * subctch_em_in.no3 + ...
                         0.7187953 * subctch_em_in.flow;
    
    % (e) no2
    % -------
    subctch_em_out.no2 = 2.601129e-01 + ...
                         1.303220e+00 * subctch_em_in.no2 + ...
                        -8.291546e-05 * subctch_em_in.orgn + ...
                         9.773148e-05 * subctch_em_in.disox + ...
                         6.826006e-08 * (subctch_em_in.orgn .* subctch_em_in.disox);
    subctch_em_out.no2(subctch_em_out.no2 < 0) = 0; % censor from below zero
    
    % (f) nh4
    % -------
    subctch_em_out.nh4 = 9.140625e+00 + ...
                         1.072155e+00 * subctch_em_in.nh4 + ...
                         2.594713e-03 * subctch_em_in.orgn + ...
                        -2.047392e-03 * subctch_em_in.disox + ...
                         2.391673e-06 * (subctch_em_in.orgn .* subctch_em_in.disox);
    subctch_em_out.nh4(subctch_em_out.nh4 < 0) = 0; % censor from below zero
    
    % (g) minp
    % -------
    subctch_em_out.minp = 1.03796735 + ...
                          1.10844745 * subctch_em_in.minp + ...
                          0.01317617 * subctch_em_in.orgp;
    
    % (h) disox
    % ---------
    subctch_em_out.disox = -1.850679e+02 + ...
                            9.151463e-01 * subctch_em_in.disox + ...
                           -8.175884e+00 * subctch_em_in.flow + ...
                            3.651789e-02 * subctch_em_in.orgn + ...
                           -2.820580e-01 * subctch_em_in.orgp + ...
                           -1.655900e-04 * (subctch_em_in.flow .* subctch_em_in.orgn) + ...
                            1.256354e-03 * (subctch_em_in.flow .* subctch_em_in.orgp) + ...
                            9.945690e-08 * (subctch_em_in.orgn .* subctch_em_in.orgp) + ...
                           -3.346412e-10 * (subctch_em_in.flow .* subctch_em_in.orgn .* subctch_em_in.orgp);
    subctch_em_out.disox(subctch_em_out.disox < 0) = 0; % censor from below zero
end